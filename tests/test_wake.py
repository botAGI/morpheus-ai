"""
Tests for morpheus.core.wake
"""
from datetime import datetime, timezone
from morpheus.core.wake import generate_wake_md
from morpheus.core.models import Source, Claim, ProjectState


def test_generate_wake_md_basic():
    source = Source(
        id="src_001",
        path="README.md",
        kind="markdown",
        sha256="abc123",
        size_bytes=100,
        line_count=10,
        modified_at=datetime.now(timezone.utc)
    )
    
    claim = Claim(
        id="clm_001",
        source_id="src_001",
        line_start=5,
        line_end=5,
        excerpt="TODO: implement feature X",
        category="task",
        created_at=datetime.now(timezone.utc)
    )
    
    state = ProjectState(
        sources=[source],
        claims=[claim],
        evidence=[],
        compiled_at=datetime.now(timezone.utc),
        receipt_id="rcpt_test_001"
    )
    
    wake = generate_wake_md(state, "rcpt_test_001")
    
    assert "# WAKE.md" in wake
    assert "rcpt_test_001" in wake
    assert "### Open Tasks" in wake
    assert "implement feature X" in wake
    assert "Generated by Morpheus" in wake
    assert "morpheus verify --all" in wake
    assert "morpheus verify --provenance" not in wake


