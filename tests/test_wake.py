def test_generate_wake_md_lists_sources_referenced_by_active_claims():
    sources = [
        Source(
            id="src_001",
            path="README.md",
            kind="markdown",
            sha256="abc123",
            size_bytes=100,
            line_count=10,
            modified_at=datetime.now(timezone.utc),
        ),
        Source(
            id="src_002",
            path="docs/archive.md",
            kind="markdown",
            sha256="def456",
            size_bytes=200,
            line_count=20,
            modified_at=datetime.now(timezone.utc),
        ),
    ]
    claims = [
        Claim(
            id="clm_001",
            source_id="src_001",
            line_start=5,
            line_end=5,
            excerpt="TODO: document source references",
            category="task",
            status="active",
            created_at=datetime.now(timezone.utc),
        ),
        Claim(
            id="clm_002",
            source_id="src_002",
            line_start=3,
            line_end=3,
            excerpt="TODO: archived task",
            category="task",
            status="resolved",
            created_at=datetime.now(timezone.utc),
        ),
    ]
    state = ProjectState(
        sources=sources,
        claims=claims,
        evidence=[],
        compiled_at=datetime.now(timezone.utc),
        receipt_id="rcpt_sources",
    )

    wake = generate_wake_md(state, "rcpt_sources")

    assert "## Source References" in wake
    assert "- src_001: `README.md`" in wake.splitlines()
    assert "src_002" not in wake
    assert "docs/archive.md" not in wake


